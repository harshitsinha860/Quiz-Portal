    public void setTime(int Minutes, int Seconds){
        if(Minutes != -1)
            time.setText(Minutes + ":" + Seconds);
        else
        {
            try{
            ObjectOutputStream objectOutputStream =new ObjectOutputStream(socket.getOutputStream());
            String[] s = {"sectioncompletemcqs",qcode,username,mcqsscore+"","0"};
            objectOutputStream.writeObject(s);
            sectionStudent.flags = 1;
            sectionStudent.s1.setVisible(false);
            dispose();
            }
            catch(IOException e){
            e.printStackTrace();
            }
            JOptionPane.showMessageDialog(null, "Time-Over!");
            count = -1;
            tt.stop();
        }
    }
    private void submitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_submitMouseClicked
           String userans="E";
           if(opt1.isSelected())
               userans="A";
           else if(opt2.isSelected())
               userans="B";
           else if(opt3.isSelected())
               userans="C";
           else if(opt4.isSelected())
               userans="D";
           if(userans.equals(questionmcqs[currentmcqs][5])){
                mcqsscore++;
           }
           System.out.println(questionmcqs[currentmcqs][5]); 
           currentmcqs++;
          
           if(currentmcqs != totalquestionsmcqs)
           {
             //  System.out.println("Q.no. "+currentmcqs+" completed");
               opt1.setSelected(false);
               opt2.setSelected(false);
               opt3.setSelected(false);
               opt4.setSelected(false);
               question.setText(questionmcqs[currentmcqs][0]);
               opt1.setText(questionmcqs[currentmcqs][1]);
               opt2.setText(questionmcqs[currentmcqs][2]);
               opt3.setText(questionmcqs[currentmcqs][3]);
               opt4.setText(questionmcqs[currentmcqs][4]); 
           }
           else
           {
               question.setText("SECTION OVER!");
               opt1.setText("N/A");
               opt2.setText("N/A");
               opt3.setText("N/A");
               opt4.setText("N/A");
               JOptionPane.showMessageDialog(null,"Section is over, Click on END SECTION!");   
           }             
             
    }//GEN-LAST:event_submitMouseClicked

