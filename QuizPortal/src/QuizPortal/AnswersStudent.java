class timeThread implements Runnable{
    //private static Thread t;
    private static AnswersStudent Rk;
    private static int TotalMinutes;
    private static int TotalSeconds;
    private static Thread t;
    public timeThread(int time, AnswersStudent Rk){
        this.Rk = Rk;
        TotalMinutes = time/60;
        TotalSeconds = time%60;
        t = new Thread(this);
    }
    @Override public void run(){
        int Minute=TotalMinutes;
        int Second=TotalSeconds;
        System.out.println("Minute "+Minute);
        while(true){
                try{
                     Rk.setTime(Minute, Second);
                     t.sleep(1000);
                     System.out.println(Minute+" "+Second);
                     Second--;
                     System.out.println(Minute+" "+Second);
                     if(Second==-1){
                         Minute--;
                         Second=59;
                                 
                     }
                }catch(Exception e){
                    e.printStackTrace();
                }
            }      
        }
    public void start(){
        t.start();
    }
    public void stop(){
        t.stop();
    }
    public void suspend(){
        t.suspend();
    }
    public void resume(){
        t.resume();
    }
}

