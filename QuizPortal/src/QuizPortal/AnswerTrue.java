    public void setTimetf(int Minutes, int Seconds){
        if(Minutes!=-1)
            time.setText(Minutes + ":" + Seconds);
        else{
            try{
            ObjectOutputStream objectOutputStream =new ObjectOutputStream(socket.getOutputStream());
            String[] s = {"sectioncompletetf",qcode,username,tfscore+"","0"};
            objectOutputStream.writeObject(s);
            sectionStudent.flagt = 1;
            sectionStudent.t1.setVisible(false);
            dispose();
            }catch(IOException e){
            e.printStackTrace();
            }
            JOptionPane.showMessageDialog(null, "Time-Over!");
            count = -1;
            ttt.stop();
        }
    }
    private void submitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_submitMouseClicked
        // TODO add your handling code here:
        String userans="";
        String s1 = tf.getText();
        if(s1.equals("T"))
            userans="T";
        else if(s1.equals("F"))
            userans="F";
         if(userans.equals(questiontf[currenttf][1])){
                tfscore++;
         }
         //System.out.println(questiontf[currenttf][1]);
          currenttf++;
           if(currenttf!=totalquestionstf)
           {
               question.setText(questiontf[currenttf][0]);
               tf.setText("N/A");    
           }
           else
           {
               JOptionPane.showMessageDialog(null,"Section is over, Click on END SECTION!");
               
           }             
             
    }//GEN-LAST:event_submitMouseClicked
    
    
